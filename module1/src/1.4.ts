// Basic data type:

// string
let firstName: string = "Shahadad";

// Number
let roll: number = 23;

// boolean
let isAdmin: boolean = true;

// undefined
let x: undefined = undefined;

// null
let y: null = null;

// any
let d;

d = 123;
d = 'string';

// We can defined the data type before we store the value;

let b: number;

b = 123;


// ----- Non Primitive data:

// Array
let friends: string[] = ['Rafi', 'Rifat'];

let eligibleRoll: number[] = [1, 2, 3, 4];


// Tuple ----> Array ----> Order ----> type of values;

let coordinates: [number, number] = [1, 5];

let ageName: [number, string] = [50, 'Mr Rohid'];

let newUser: [number, string, boolean] = [50, 'Shahadad', true];